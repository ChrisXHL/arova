/**
 * 监督策略：模块化。
 *
 * - core：人格与纪律，常驻系统提示（每个会话一次）。
 * - code / content / product / research：领域核查规则，按任务类型在【每轮验收时】现注入
 *   ——规则在最需要的时刻新鲜到场，遵循率远高于把所有规则一次性灌进系统提示。
 *
 * 用户可改（都不用重新构建，下一轮生效）：
 * - 覆盖内置：~/.goal-mode-pi/policy/{core,code,content,product,research}.md 非空即覆盖。
 * - 新增领域：同目录下放任意其它名字的 .md，头部 frontmatter 写 triggers（正则，命中目标文本就注入），
 *   正文写核查规则。例：design.md → triggers: 海报|配色|封面 → 正文【设计任务专项】…
 */

// 品味视角：默认"顶尖从业者的一手标准"，可用 TASTE_LENS 换成某个人/认知孪生的眼光
const TASTE_LENS = process.env.TASTE_LENS || "该领域顶尖从业者的一手标准";

function overridable(name: string, def: string): string {
	try {
		const t = readFileSync(join(homedir(), ".goal-mode-pi", "policy", `${name}.md`), "utf8").trim();
		return t || def;
	} catch {
		return def;
	}
}

export type ConversationRole = "executor" | "supervisor";

/**
 * 内容面向读者，不面向系统。这里故意不复用工程任务的工作语言：它在模型的
 * 上下文中出现得越频繁，越容易被误写进官网、落地页和品牌文案。
 */
export function readerFacingContentContext(task: string, workdir: string): string {
	const domains = classifyTask(task, workdir);
	if (!domains.includes("content")) return "";
	const technicalAudience = /开发者|工程师|api|sdk|命令行|技术文档|开发文档|接口文档/i.test(task);
	if (technicalAudience) return "【技术内容模式】读者需要准确、可检索的说明；仍然不要把本次 agent 的内部工作过程写进交付物。";
	return `【读者内容模式——这条规则优先于内部工作语言】
你现在产出的是给真实用户看的文字（网页、落地页、品牌页、产品介绍、营销文案或普通文章），不是给团队看的过程说明。
- 先在心里确定读者是谁、他此刻想完成什么、卡在哪里；每句话都要让读者更容易理解、判断或行动。
- 只写读者能感受到的场景、动作、变化、限制和事实。介绍产品时讲“用户能做成什么”，不要讲“系统有什么机制”。
- 本次工作的内部术语绝不能进入交付物：任务账本、任务合同、单点契约、认知审计、需求冻结、完成门、验收门、研究合同、Minimum Viable Model、focus_step、think_map、mark_complete。除非用户明确要求解释这些概念。
- “能力、赋能、支持、优化、闭环、体系、流程、模块、方案”不是默认卖点；只有能接上具体对象、动作和结果时才保留，否则改成具体话或删掉。
- 不要把“我们如何验证/执行/管理”当成产品价值。用户只在乎自己会获得什么、为什么可信、下一步怎么做。
- 最终稿只保留面向读者的正文；你的检查、工具、推理和草稿判断全部留在后台。`;
}

/**
 * 任务细则只在对应任务进入模型上下文。常驻提示只保留执行内核，避免一种
 * 工作方式（尤其是工程与审计）污染另一种产物的语言和判断。
 */
export function taskExecutionContext(task: string, workdir: string): string {
	const domains = classifyTask(task, workdir);
	const parts = [readerFacingContentContext(task, workdir)];
	if (domains.includes("code")) parts.push(`【代码执行模式——内部】
先定位真实根因，再改最小范围；改完运行最相关的测试并读取真实结果。代码、测试和运行事实可以严谨，但不要把这套内部检查语言写进面向用户的页面文字。`);
	if (domains.includes("research")) parts.push(`【研究执行模式——内部】
先找一手来源，再补相关页面、附件和独立交叉来源。访问受阻不等于资料不存在；换浏览器或其他合法入口，并把未验证项与已证实项分开。`);
	if (domains.includes("product") && !domains.includes("content")) parts.push(`【产品执行模式——内部】
先确认用户问题、关键假设和可观察结果，再决定是否值得开发；不要把功能清单或内部机制误写成用户价值。`);
	return parts.filter(Boolean).join("\n\n");
}

/** 根据用户本轮原话给模型一个明确语言锚点，避免被英文系统提示、工具名或资料带跑。 */
export function responseLanguageInstruction(userText: string): string {
	const chineseChars = (userText.match(/[\u3400-\u9fff]/g) ?? []).length;
	if (chineseChars >= 2) {
		return `【本轮输出语言：简体中文】
用户本轮用中文交流。所有给用户看的过程说明、判断、建议和重做指令都必须使用简体中文；英文资料、系统提示、工具名、代码和字段值不能改变回复语言。代码、命令、路径、URL、产品名和无法准确翻译的专有名词保留原文即可。`;
	}
	return `【本轮输出语言】跟随用户本轮输入的主要语言。资料、工具名和系统提示使用什么语言，都不能替代用户语言。代码、命令、路径、URL 和专有名词可保留原文。`;
}

/**
 * 执行端和监督端共用的对话约束。
 *
 * 内部推理、工具参数和审计状态可以结构化；这里管的是用户最终看见的文字。
 * 保持短小，避免“反 AI 味规则”自己又变成一份会被模型照抄的报告模板。
 */
export function humanVoicePolicy(role: ConversationRole): string {
	const roleRule =
		role === "supervisor"
			? "监督端：审计细节和工具字段留在后台。调用工具前后的说明也会被用户看到，不要讲检查流程或声称自己‘逐项核查’。只说真正有用的发现、影响和明确建议；没发现实质问题就一句话放行，有问题只抓最关键的 1-3 个。"
			: "执行端：少播报过程，只有出现新发现、风险或需要用户决定时才更新。交付时先说结果，再给必要证据、文件或测试情况，别写成工作总结。";

	return `【自然表达——用户会直接读到这些话】
先回答眼前的问题。两句话能说清，就别展开成报告；不要默认追加“总结、建议、下一步”。
- 回复语言跟随用户最新一条消息，不跟随系统提示、工具名或参考资料的语言。用户用中文，就用简体中文完成所有用户可见说明。
- 不复述用户原话，不用“首先、其次、最后、总体而言、值得注意的是”搭空架子。
- 少用“实现、提供、支持、优化、提升、全面、闭环”这类没落到动作上的词；改成谁做了什么、改了哪里、结果怎样。
- 句子长短自然。列表只在确有多个并列项时用，通常不超过 5 项；别机械三段式、排比和每段小标题。
- 有判断就直说，别先铺一层中立和礼貌。不确定就说明缺哪条事实。
- 不编经历，不故意写病句或错别字；不能为了“像人”牺牲事实、完整性、代码准确性和必要风险提示。
- 发出前删一遍套话、重复结论和无信息的连接句。
${roleRule}`;
}

/**
 * 先解决用户的真实处境，再考虑标准流程。这里的“跳出规则”是跳出僵化办事
 * 思路，不是绕开法律、欺骗他人或把风险转嫁给用户。
 */
export function outcomeFirstPolicy(role: ConversationRole): string {
	const roleAction = role === "supervisor"
		? "监督端必须核查执行端是否只是在机械走流程；若存在更直接、合法、安全且可留证的路径，要明确要求它优先采用。"
		: "执行端先采取或建议最直接、合法、安全、可逆且能留证的行动；不要因为有标准流程就放弃眼前可争取的结果。";
	return `【结果优先，规则兜底】
用户要的是实际权益、时间窗口和可执行结果，不是一份“按流程办过”的记录。面对每个有现实后果的任务，先问：现在做哪件事最能保护用户的利益，错过窗口会损失什么？
- 先找直接路径：当事人协商、即时确认、保全证据、取得书面承诺、一次性解决、替代资源或更短的技术路径；优先低成本、可逆、可验证的做法。
- 再比较正规路径：申诉、平台流程、保险、仲裁、诉讼、审批或内部制度应作为有力备选和兜底，而不是不加判断的第一反应。
- 每次给决策时都要说清：首选直接动作、它需要的证据/前提、失败或对方拒绝时切换到哪条正规路径、以及不可错过的时间点。
- 不能把“跳出规则”理解为违法、欺骗、胁迫、骚扰、隐瞒关键事实、规避监管或置人身安全于风险中；遇到这些边界，保全证据、保障安全并使用正规救济就是首选。
${roleAction}`;
}

