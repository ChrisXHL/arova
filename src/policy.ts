export type Domain = string;

const BUILTIN = ["core", "code", "content", "product", "research"];

interface CustomDomain {
	name: string;
	triggers: RegExp;
	body: string;
}

/**
 * 自定义领域：~/.goal-mode-pi/policy/ 下任意非内置名的 .md 文件即一个新领域。
 * 文件头部用 frontmatter 声明触发词（命中目标文本就注入），正文是核查规则：
 *
 *   ---
 *   triggers: 海报|配色|字体|封面|视觉|logo|品牌
 *   ---
 *   【设计任务专项】
 *   - ...
 *
 * 没有 triggers 头的文件不会被激活（写上才生效）。改完立即生效，不用重新构建。
 */
function customDomains(): CustomDomain[] {
	try {
		const dir = join(homedir(), ".goal-mode-pi", "policy");
		return readdirSync(dir)
			.filter((f) => f.endsWith(".md"))
			.map((f) => f.slice(0, -3))
			.filter((n) => !BUILTIN.includes(n))
			.flatMap((name) => {
				try {
					const raw = readFileSync(join(dir, `${name}.md`), "utf8").trim();
					const m = raw.match(/^---\s*\ntriggers:\s*(.+?)\s*\n---\s*\n?/);
					if (!m) return [];
					const body = raw.slice(m[0].length).trim();
					if (!body) return [];
					return [{ name, triggers: new RegExp(m[1], "i"), body }];
				} catch {
					return [];
				}
			});
	} catch {
		return [];
	}
}

/** 按目标文本 + 工作目录信号判断任务领域（多标签；文本无信号时才用目录信号兜底）。 */
export function classifyTask(goal: string, workdir: string): Domain[] {
	const g = goal.toLowerCase();
	const out = new Set<Domain>();
	for (const c of customDomains()) if (c.triggers.test(g)) out.add(c.name);
	if (/修复|实现|重构|debug|bug|报错|崩溃|测试|代码|函数|接口|脚本|部署|编译|依赖|安装|性能|重写|迁移/.test(g)) out.add("code");
	if (/文章|文案|写篇|改写|润色|翻译|标题|公众号|小红书|推文|博客|软文|readme|文档|说明|手册|稿|官网|落地页|首页|品牌页|产品页|营销页|宣传页|slogan|cta/.test(g)) out.add("content");
	if (/产品|功能|竞品|定价|需求|用户体验|界面|ui|ux|设计|落地页|官网|交互|原型|壳|页|按钮|开关|弹窗|菜单|导航|表单|样式|主题|深色|暗色|布局|组件|图标|动画|配色/.test(g)) out.add("product");
	if (/调研|分析|研究|对比|评估|报告|市场|数据|可行性/.test(g)) out.add("research");
	if (out.size === 0) {
		// 文本没信号：项目长什么样就先按什么验
		const has = (f: string) => existsSync(join(workdir, f));
		if (has("package.json") || has("pyproject.toml") || has("Cargo.toml") || has("go.mod") || has("Makefile")) out.add("code");
	}
	return [...out];
}

/** 组装本轮要注入的领域模块（空数组 → 空串，core 里的通用检查兜底）。 */
export function domainBlock(domains: Domain[]): string {
	const builtin: Record<string, string> = {
		code: overridable("code", CODE),
		content: overridable("content", CONTENT),
		product: overridable("product", PRODUCT),
		research: overridable("research", RESEARCH),
	};
	const custom = new Map(customDomains().map((c) => [c.name, c.body]));
	const parts = domains.map((d) => builtin[d] ?? custom.get(d) ?? "").filter(Boolean);
	return parts.length ? `\n${parts.join("\n\n")}\n` : "";
}

